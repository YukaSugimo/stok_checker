# 分析パラメータを管理する設定ファイル
# 取得日数・移動平均日数・買いシグナル閾値などを管理する
# ロジックを調整したいときはこのファイルだけ修正する
# 注意: FETCH_DAYS は MA_DAYS より大きい値を設定すること

# 株価データの取得日数（日本株の営業日ベース）
# MA_DAYSより大きい値である必要がある
FETCH_DAYS = 90

# 移動平均の計算日数
# 25日は国内株で一般的な短期移動平均
MA_DAYS = 25

# 買いシグナルと判定する閾値（小数表記）
# 0.02 = 2%：ノイズ除去のための最小限の閾値として仮設定
# 判定ロジック: 直近終値 > 移動平均 × (1 + BUY_THRESHOLD) の場合にシグナルあり
# 運用しながら調整する
BUY_THRESHOLD = 0.02
