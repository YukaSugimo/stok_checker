# 分析パラメータを管理する設定ファイル
# 取得日数・移動平均日数・買いシグナル閾値などを管理する
# ロジックを調整したいときはこのファイルだけ修正する

# ========================================
# Phase 1: データ取得パラメータ
# ========================================

# 株価データの取得日数（日本株の営業日ベース）
# MA_DAYSより大きい値である必要がある（起動時にバリデーション済み）
FETCH_DAYS = 90

# 移動平均の計算日数
# 25日は国内株で一般的な短期移動平均
MA_DAYS = 25

# 買いシグナルと判定する閾値（小数表記）
# 0.02 = 2%：ノイズ除去のための最小限の閾値として仮設定
# 判定ロジック: 直近終値 > 移動平均 × (1 + BUY_THRESHOLD) の場合にシグナルあり
# 運用しながら調整する
BUY_THRESHOLD = 0.02

# ========================================
# Phase 2: Discord通知パラメータ
# ========================================

# Discord Webhook へのHTTPリクエストタイムアウト秒数
DISCORD_TIMEOUT_SEC = 10

# Discord通知失敗時のリトライ回数
DISCORD_RETRY_COUNT = 3

# ========================================
# パラメータバリデーション
# ========================================

# FETCH_DAYS が MA_DAYS 以下の場合、移動平均の計算に必要なデータが不足する
if FETCH_DAYS <= MA_DAYS:
    raise ValueError(
        f"FETCH_DAYS ({FETCH_DAYS}) は MA_DAYS ({MA_DAYS}) より大きい値にしてください。"
        f"移動平均を計算するのに十分なデータが取得できません。"
    )
